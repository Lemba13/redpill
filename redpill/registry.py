"""
registry.py — Dimension registry with HyDE-native embeddings.

Manages the dimension_registry table: registration, similarity-based
resolution, and the topic coverage scaffold used by the planner prompt.

Each research dimension is represented as the centroid of N HyDE
(Hypothetical Document Embedding) abstracts generated by the planner LLM.
New dimension candidates are compared against the registry centroid and
merged if cosine similarity exceeds the resolution threshold (default 0.88).

Public API:
    compute_dim_id(canonical_name, topic) -> str
    generate_hyde_abstract(dimension_name, topic, ollama_client) -> str
    embed_hyde_abstract(abstract_text) -> np.ndarray
    register_dimension(canonical_name, topic, ollama_client, conn, n_abstracts) -> str
    resolve_or_register(candidate_str, topic, ollama_client, conn, threshold, n_abstracts) -> str
    get_all_registry_embeddings(conn) -> list[tuple[str, np.ndarray]]
    get_registry_size(conn) -> int
    get_all_registry_dims_for_prompt(topic, conn) -> list[dict]
    get_dimension_axis_tags(dim_id, topic, conn) -> dict
    generate_topic_scaffold(topic, ollama_client, conn) -> dict
    update_registry_embedding(dim_id, conn) -> None  [stub]
"""

import hashlib
import json
import logging
import re
from datetime import date as _date
from typing import TYPE_CHECKING

import numpy as np

from redpill.dedup import compute_embedding
from redpill.state import deserialize_embedding, serialize_embedding

if TYPE_CHECKING:
    import sqlite3

    from redpill.summarize import PlannerLLMClient

logger = logging.getLogger(__name__)

_HYDE_SYSTEM_PROMPT = (
    "You are a research writing assistant. "
    "Return only the abstract text — no title, no author list, no preamble."
)

_SCAFFOLD_SYSTEM_PROMPT = (
    "You are a research analyst. You return only valid JSON — no explanation, "
    "no markdown, no preamble."
)

# Strip common LLM preambles before storing the abstract text.
_PREAMBLE_RE = re.compile(
    r"^(?:abstract:?\s*|here is[^\n.]*[.\n]\s*|certainly[^\n.]*[.\n]\s*|sure[^\n.]*[.\n]\s*)",
    re.IGNORECASE,
)

_MAX_HYDE_TOKENS = 250


# ---------------------------------------------------------------------------
# dim_id hashing — single source of truth for the entire codebase
# ---------------------------------------------------------------------------


def compute_dim_id(canonical_name: str, topic: str) -> str:
    """Return a stable dim_id for a named dimension within a topic.

    Uses lowercased, stripped inputs with '|' separator to prevent hash
    collisions and ensure case-insensitive identity.

    Format: "dim_" + first 12 hex chars of SHA-256(name|topic).

    Note: the formula used before registry.py was introduced lacked the
    separator and lowercasing. Old rows produced by that formula will not
    match new rows; they age out naturally as the registry fills in.
    """
    raw = (
        canonical_name.lower().strip() + "|" + topic.lower().strip()
    ).encode("utf-8")
    return "dim_" + hashlib.sha256(raw).hexdigest()[:12]


# ---------------------------------------------------------------------------
# HyDE abstract generation and embedding
# ---------------------------------------------------------------------------


def generate_hyde_abstract(
    dimension_name: str,
    topic: str,
    ollama_client: "PlannerLLMClient",
) -> str:
    """Call the planner LLM to produce a ~150-word hypothetical abstract.

    Uses think_override=False and json_format=False because the output is
    free text, not JSON, and thinking overhead is wasteful here.

    Raises RuntimeError on LLM failure — do not swallow. A bad embedding
    is worse than no embedding.
    """
    prompt = (
        "Write a 150-word abstract for a research paper that represents the kind of\n"
        'work covered by the research dimension "' + dimension_name + '" within the\n'
        'broader topic of "' + topic + '".\n\n'
        "The abstract should follow this structure:\n"
        "- Problem: what challenge or gap the work addresses\n"
        "- Method: the approach or technique used\n"
        "- Evaluation: how the work is measured or validated\n"
        "- Domain: the application area or field\n\n"
        "Constraints:\n"
        "- Do not cite or reference specific existing papers\n"
        "- Do not invent specific numeric results (e.g. no '94.3% accuracy')\n"
        "- Use concrete, domain-specific vocabulary — methods, datasets, metrics\n"
        "  that would plausibly appear in papers on this dimension\n"
        "- Write in third person, present tense, as if describing the paper's contribution\n\n"
        "Return only the abstract text. No title, no author list, no preamble."
    )
    raw = ollama_client.generate(
        prompt,
        system=_HYDE_SYSTEM_PROMPT,
        think_override=False,
        json_format=False,
    )
    text = _PREAMBLE_RE.sub("", raw.strip()).strip()
    return text


def embed_hyde_abstract(abstract_text: str) -> np.ndarray:
    """Embed a HyDE abstract using the all-MiniLM-L6-v2 singleton.

    Truncates to 250 whitespace-delimited tokens before encoding.
    Returns an L2-normalized float32 numpy array.
    """
    tokens = abstract_text.split()
    if len(tokens) > _MAX_HYDE_TOKENS:
        abstract_text = " ".join(tokens[:_MAX_HYDE_TOKENS])

    arr = compute_embedding(abstract_text)
    norm = np.linalg.norm(arr)
    if norm == 0.0:
        return arr
    return (arr / norm).astype(np.float32)


