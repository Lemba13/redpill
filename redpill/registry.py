# Registry CRUD
# ---------------------------------------------------------------------------


def register_dimension(
    canonical_name: str,
    topic: str,
    ollama_client: "PlannerLLMClient",
    conn: "sqlite3.Connection",
    n_abstracts: int = 3,
) -> str:
    """Generate n_abstracts HyDE abstracts, average their embeddings, and
    INSERT OR IGNORE the centroid into dimension_registry.

    Returns dim_id. Raises RuntimeError if any abstract generation fails —
    a partial average would silently corrupt the centroid.
    """
    embeddings: list[np.ndarray] = []
    abstracts: list[str] = []

    for i in range(n_abstracts):
        abstract = generate_hyde_abstract(canonical_name, topic, ollama_client)
        abstracts.append(abstract)
        emb = embed_hyde_abstract(abstract)
        embeddings.append(emb)
        logger.debug(
            "register_dimension: abstract %d/%d for %r (%d chars)",
            i + 1,
            n_abstracts,
            canonical_name,
            len(abstract),
        )

    centroid = np.mean(np.stack(embeddings), axis=0)
    norm = np.linalg.norm(centroid)
    if norm > 0.0:
        centroid = centroid / norm
    centroid = centroid.astype(np.float32)

    dim_id = compute_dim_id(canonical_name, topic)
    blob = serialize_embedding(centroid)

    conn.execute(
        """
        INSERT OR IGNORE INTO dimension_registry
            (dim_id, canonical_name, topic, embedding, hyde_abstract,
             pool, alpha, beta, run_count)
        VALUES (?, ?, ?, ?, ?, 'explore', 1, 1, 0)
        """,
        (dim_id, canonical_name, topic, blob, abstracts[0]),
    )
    logger.info(
        "register_dimension: %r → %s (INSERT OR IGNORE)", canonical_name, dim_id
    )
    return dim_id


def resolve_or_register(
    candidate_str: str,
    topic: str,
    ollama_client: "PlannerLLMClient",
    conn: "sqlite3.Connection",
    threshold: float = 0.88,
    n_abstracts: int = 3,
) -> str:
    """Resolve candidate_str to an existing dim_id or register it as new.

    Generates a single HyDE abstract for the similarity probe (resolution
    doesn't need a centroid — one sample is enough to locate it in space).
    If cosine similarity to the closest registry entry exceeds threshold,
    returns that dim_id. Otherwise registers a new dimension with n_abstracts.

    Returns dim_id in both cases.
    """
    abstract = generate_hyde_abstract(candidate_str, topic, ollama_client)
    query_vec = embed_hyde_abstract(abstract)

    registry = get_all_registry_embeddings(conn)

    best_id: str | None = None
    best_sim: float = -1.0

    for dim_id, vec in registry:
        sim = float(np.dot(query_vec, vec))
        if sim > best_sim:
            best_sim = sim
            best_id = dim_id

    decision = "resolved" if (best_id is not None and best_sim > threshold) else "new"
    logger.debug(
        "resolve_or_register: candidate=%r similarity=%.4f → %s dim_id=%r",
        candidate_str,
        best_sim,
        decision,
        best_id,
    )

    if best_id is not None and best_sim > threshold:
        return best_id

    return register_dimension(
        candidate_str, topic, ollama_client, conn, n_abstracts=n_abstracts
    )


def get_all_registry_embeddings(
    conn: "sqlite3.Connection",
) -> list[tuple[str, np.ndarray]]:
    """Return (dim_id, embedding) pairs from dimension_registry.

    Excludes the system sentinels (dim_fallback, dim_base) and rows with
    NULL embedding.
    """
    rows = conn.execute(
        """
        SELECT dim_id, embedding FROM dimension_registry
        WHERE embedding IS NOT NULL
          AND dim_id NOT IN ('dim_fallback', 'dim_base')
        """
    ).fetchall()

    result: list[tuple[str, np.ndarray]] = []
    for row in rows:
        try:
            arr = deserialize_embedding(row["embedding"])
            result.append((row["dim_id"], arr))
        except Exception as exc:
            logger.warning(
                "get_all_registry_embeddings: failed to deserialize dim_id=%r: %s",
                row["dim_id"],
                exc,
            )
    return result


def get_registry_size(conn: "sqlite3.Connection") -> int:
    """Return count of non-system rows in dimension_registry."""
    row = conn.execute(
        """
        SELECT COUNT(*) FROM dimension_registry
        WHERE dim_id NOT IN ('dim_fallback', 'dim_base')
        """
    ).fetchone()
    return row[0] if row else 0


# ---------------------------------------------------------------------------
# Axis tags and coverage map helpers
# ---------------------------------------------------------------------------


def _run_count_to_coverage(run_count: int) -> str:
    if run_count < 3:
        return "sparse"
    if run_count <= 8:
        return "moderate"
    return "high"


def get_dimension_axis_tags(
    dim_id: str,
    topic: str,
    conn: "sqlite3.Connection",
) -> dict:
    """Return axis tags for a registered dimension.

    Joins extracted_terms with seen_items on URL to find terms attributed
    to this dimension via seen_items.dim_id. Falls back gracefully when
    seen_items.dim_id is NULL (rows ingested before dim_id plumbing).

    Returns dict with keys: primary_axis (str), tags (list[str]), coverage (str).
    """
    rows = conn.execute(
        """
        SELECT et.category, et.term, COUNT(*) AS freq
        FROM extracted_terms et
        JOIN seen_items si ON et.source_url = si.url
        WHERE si.dim_id = ? AND et.topic = ?
        GROUP BY et.category, et.term
        ORDER BY freq DESC
        LIMIT 20
        """,
        (dim_id, topic),
    ).fetchall()

    reg_row = conn.execute(
        "SELECT run_count FROM dimension_registry WHERE dim_id = ?", (dim_id,)
    ).fetchone()
    run_count = reg_row["run_count"] if reg_row else 0
    coverage = _run_count_to_coverage(run_count)

    if not rows:
        return {"primary_axis": "unknown", "tags": [], "coverage": coverage}

    category_counts: dict[str, int] = {}
    all_terms: list[str] = []
    for row in rows:
        cat = row["category"] or "unknown"
        category_counts[cat] = category_counts.get(cat, 0) + row["freq"]
        all_terms.append(row["term"])

    primary_axis = max(category_counts, key=category_counts.__getitem__)
    return {
        "primary_axis": primary_axis,
        "tags": all_terms[:3],
        "coverage": coverage,
    }


def get_all_registry_dims_for_prompt(
    topic: str,
    conn: "sqlite3.Connection",
) -> list[dict]:
    """Return all registered dimensions for a topic with axis annotations.

    Used by _build_decompose_prompt to construct the coverage map section.
    Each entry has: dim_id, canonical_name, run_count, primary_axis, tags, coverage.
    """
    rows = conn.execute(
        """
        SELECT dim_id, canonical_name, run_count
        FROM dimension_registry
        WHERE dim_id NOT IN ('dim_fallback', 'dim_base')
          AND topic = ?
        ORDER BY run_count DESC
        """,
        (topic,),
    ).fetchall()

    result: list[dict] = []
    for row in rows:
        axis_info = get_dimension_axis_tags(row["dim_id"], topic, conn)
        result.append(
            {
                "dim_id": row["dim_id"],
                "canonical_name": row["canonical_name"],
                "run_count": row["run_count"],
                **axis_info,
            }
        )
    return result


