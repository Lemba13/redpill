def generate_topic_scaffold(
    topic: str,
    ollama_client: "PlannerLLMClient",
    conn: "sqlite3.Connection",
) -> dict:
    """Generate or return cached topic coverage scaffold.

    Checks topic_scaffold table first. If present, returns the cached dict
    without calling the LLM. If absent, calls the planner LLM (think=True,
    json_format=True) and stores the result with INSERT OR REPLACE.

    The scaffold decomposes the topic into five axes — methodological, domain,
    evaluation, theoretical, application — each listing 4-8 concrete families.
    """
    from redpill.llm_utils import extract_json

    row = conn.execute(
        "SELECT scaffold FROM topic_scaffold WHERE topic = ?", (topic,)
    ).fetchone()

    if row is not None:
        try:
            scaffold = json.loads(row["scaffold"])
            logger.debug(
                "generate_topic_scaffold: returning cached scaffold for %r", topic
            )
            return scaffold
        except json.JSONDecodeError as exc:
            logger.warning(
                "generate_topic_scaffold: cached scaffold is invalid JSON (%s) — regenerating",
                exc,
            )

    prompt = (
        'You are analyzing the research topic: "' + topic + '"\n\n'
        "Decompose this topic into major coverage axes. For each axis, list the\n"
        "primary families, paradigms, or communities that exist within it.\n\n"
        "Return a JSON object with exactly these five keys:\n"
        '- "methodological": technique families and algorithmic approaches\n'
        '- "domain": application areas and scientific fields\n'
        '- "evaluation": evaluation paradigms and benchmark types\n'
        '- "theoretical": theoretical angles and formal frameworks\n'
        '- "application": downstream applications and deployment contexts\n\n'
        "Each value is a list of 4-8 specific, concrete entries.\n"
        "Return only the JSON object."
    )

    raw = ollama_client.generate(prompt, system=_SCAFFOLD_SYSTEM_PROMPT)
    parsed = extract_json(raw)

    _SCAFFOLD_KEYS = ("methodological", "domain", "evaluation", "theoretical", "application")

    if not isinstance(parsed, dict):
        logger.warning(
            "generate_topic_scaffold: LLM returned non-dict (%s) — using empty scaffold",
            type(parsed).__name__,
        )
        parsed = {k: [] for k in _SCAFFOLD_KEYS}

    # Ensure all five keys exist with list values.
    for key in _SCAFFOLD_KEYS:
        if key not in parsed or not isinstance(parsed[key], list):
            parsed[key] = []

    scaffold_json = json.dumps(parsed, ensure_ascii=False)
    today = _date.today().isoformat()
    conn.execute(
        """
        INSERT OR REPLACE INTO topic_scaffold (topic, scaffold, created_at)
        VALUES (?, ?, ?)
        """,
        (topic, scaffold_json, today),
    )
    logger.info("generate_topic_scaffold: generated and stored scaffold for %r", topic)
    return parsed


